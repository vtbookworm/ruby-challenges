# my_math_method takes two integers as 
# arguments and adds them, returning the result.

def my_math_method(num1, num2)
	answer = num1 + num2
	puts "The sum of #{num1} and #{num2} is #{answer}."
end

my_math_method(2, 2)
my_math_method(5, 3)
my_math_method(107, 258)
my_math_method(-25, -5)
my_math_method(-30, 30)